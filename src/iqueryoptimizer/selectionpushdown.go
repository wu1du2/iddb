package iqueryoptimizer

import (
	"iplan"
)

func JointConditionPushDown(oldtree iplan.PlanTree) iplan.PlanTree {
	var newtree iplan.PlanTree
	newtree = oldtree
	return newtree
}

func SelectionPushDown(oldtree iplan.PlanTree) iplan.PlanTree {
	var newtree iplan.PlanTree
	newtree = oldtree
	return newtree
}
